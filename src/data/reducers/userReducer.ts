export const userReducer = (state: UserType, action: reducerActionType) => {

    switch(action.type){
        case 'CHANGE_DATA':   
        return {...state,
            id: action.payload.id,
            id_telegram: action.payload.id_telegram, 
            name: action.payload.name, 
            sobre_name: action.payload.sobre_name,  
            email: action.payload.email,  
            email_recebedor: action.payload.email_recebedor,          
            telefone: action.payload.telefone, 
            tokensenha: action.payload.tokensenha,  
            avatar_url: action.payload.avatar_url,  
            cpf: action.payload.cpf,
            rede_social1: action.payload.rede_social1, 
            rede_social2: action.payload.rede_social2, 
            bio_perfil: action.payload.bio_perfil,  
            status: action.payload.status, 
            cep: action.payload.cep, 
            endereco: action.payload.endereco,  
            complemento: action.payload.complemento, 
            bairro: action.payload.bairro, 
            numero: action.payload.numero, 
            cidade: action.payload.cidade, 
            estado: action.payload.estado, 
            razao_social: action.payload.razao_social,  
            descricao_negocio: action.payload.descricao_negocio, 
            nome_conta: action.payload.nome_conta, 
            nome_recebedor: action.payload.nome_recebedor,
            tipo_recebedor: action.payload.tipo_recebedor, 
            cpf_cnpj: action.payload.cpf_cnpj, 
            banco: action.payload.banco, 
            agencia: action.payload.agencia, 
            digito_ag: action.payload.digito_ag,
            conta: action.payload.conta, 
            digito_cc: action.payload.digito_cc,
            autenticada: action.payload.autenticada, 
            tipo_conta: action.payload.tipo_conta,
            };                             
        break;        
    }

    return state;
}